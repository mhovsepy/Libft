#include "libft.h"

int	ft_atoi(const char *str)

{
	int	v;
	int	m;

	v = 0;
	m = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			m *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		v = v * 10 + *str - '0';
		str++;
	}
	return (v * m);
}
