#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*str;
	size_t	len;

	len = ft_strlen(s);
	str = malloc(len + 1);
	i = 0;
	if (!s || !str || !f)
		return (0);
	while (i < len)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = 0;
	return (str);
}
